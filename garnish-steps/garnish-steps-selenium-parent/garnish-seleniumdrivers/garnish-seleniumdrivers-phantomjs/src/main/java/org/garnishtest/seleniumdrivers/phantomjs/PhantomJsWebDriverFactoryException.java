package org.garnishtest.seleniumdrivers.phantomjs;

public final class PhantomJsWebDriverFactoryException extends RuntimeException {

    public PhantomJsWebDriverFactoryException() {
    }

    public PhantomJsWebDriverFactoryException(final String message) {
        super(message);
    }

    public PhantomJsWebDriverFactoryException(final String message, final Throwable cause) {
        super(message, cause);
    }

    public PhantomJsWebDriverFactoryException(final Throwable cause) {
        super(cause);
    }

    public PhantomJsWebDriverFactoryException(final String message, final Throwable cause, final boolean enableSuppression, final boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

}
