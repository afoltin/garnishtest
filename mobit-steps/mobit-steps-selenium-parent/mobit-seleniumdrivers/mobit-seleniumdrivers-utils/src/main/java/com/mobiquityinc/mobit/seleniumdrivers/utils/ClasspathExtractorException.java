package com.mobiquityinc.mobit.seleniumdrivers.utils;

public final class ClasspathExtractorException extends RuntimeException {

    public ClasspathExtractorException() {
    }

    public ClasspathExtractorException(final String message) {
        super(message);
    }

    public ClasspathExtractorException(final String message, final Throwable cause) {
        super(message, cause);
    }

    public ClasspathExtractorException(final Throwable cause) {
        super(cause);
    }

    public ClasspathExtractorException(final String message, final Throwable cause, final boolean enableSuppression, final boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

}
